    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        int i=jList1.getSelectedIndex();
        if(i>=0)
        {
            String dataAlta = (String) jList1.getSelectedValue();
            dataAlta = StringUtils.BeforeLast(dataAlta, " :");
            
            modelList2.clear();
            //Carrega alumnes que han estat donats d'alta aquest dia
            String SQL1 = "SELECT CONCAT(llinatge1,' ',llinatge2,', ',nom1,' (',xh.Estudis,' ',xh.Grup,')') "+
                    " AS txt FROM `" + CoreCfg.core_mysqlDBPrefix + "`.xes_alumne AS xes INNER JOIN `" + 
                    CoreCfg.core_mysqlDBPrefix + "`.xes_alumne_historic AS xh "+
                    " ON xh.Exp2=xes.Exp2 AND xh.anyAcademic="+cfg.anyAcademicFitxes+" INNER JOIN `" + 
                    CoreCfg.core_mysqlDBPrefix + "`.xes_alta AS xal ON xal.Exp2=xh.Exp2 WHERE xal.dataAlta='"+
                    dataAlta+"' AND xh.AnyAcademic="+cfg.getCoreCfg().anyAcademic+
                    " ORDER BY txt";
                  
            //System.out.println(SQL1);
             try {
                Statement st = cfg.getCoreCfg().getMysql().createStatement();
                ResultSet rs1 = cfg.getCoreCfg().getMysql().getResultSet(SQL1,st);
                while (rs1 != null && rs1.next()) {
                    modelList2.addElement(rs1.getString(1));
                }
                rs1.close();
                st.close();
            } catch (SQLException ex) {
                Logger.getLogger(AltesPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jList1ValueChanged

