    private DefaultComboBoxModel getComboEnsenyament() {
        
       DefaultComboBoxModel combo = new DefaultComboBoxModel();
       String SQL1 = "Select distinct Ensenyament from `"+CoreCfg.core_mysqlDBPrefix+"`.xes_alumne_historic where "
               + " AnyAcademic='"+cfg.anyAcademicFitxes+"' order by Ensenyament ";
               
        try {
           Statement st = cfg.getCoreCfg().getMysql().createStatement();
           ResultSet rs1 = cfg.getCoreCfg().getMysql().getResultSet(SQL1,st);
            
            while (rs1 != null && rs1.next()) {
                String nivel = rs1.getString("Ensenyament");
                combo.addElement(nivel);
            }
             if(rs1!=null) {
                rs1.close();
                st.close();
            }
        } catch (SQLException ex) {
            Logger.getLogger(CanviaGrup.class.getName()).log(Level.SEVERE, null, ex);
        }
        return combo;
        
    }

