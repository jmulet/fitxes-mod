    class LongTask extends Thread
    {
    public void run()
    {
        double status = 0;
        double inc = 100./(1.0*listmodel1.getSize());
        String any = (String) jComboBox1.getSelectedItem();

        for(int i=0; i<listmodel1.getSize(); i++)
        {
            status += inc;
            String aux = (String) listmodel1.getElementAt(i);

            aux = StringUtils.BeforeLast(aux, "]");

            aux = StringUtils.AfterLast(aux, "[");

            int exp = (int) Double.parseDouble(aux);
            BeanDadesPersonals bean = new BeanDadesPersonals(cfg.getCoreCfg().getIesClient());
            bean.createFitxaCurs(exp, any);
            jTextArea1.append("> S'ha creat la fitxa de l'alumne "+exp+" per a l'any "+any+".\n");
            jProgressBar1.setValue((int) status);
        }
    }
}

