    /**
     * Mèdode estatic:
     * Genera les fitxes que falten a l'any academic, per la llista d'alumnes
     * Si l'alumne de la llista ja té creada la fitxa, no la torna a crear
     * @param anyacademic
     * @param expds
     * @return el nombre de fitxes creades
     */
    public static int generaFitxes(String anyacademic, ArrayList<Integer> expds, Cfg cfg) {
         
        int n = 0;
        
        for(int i=0; i<expds.size(); i++)
        {
            BeanDadesPersonals bean = new BeanDadesPersonals(cfg.getCoreCfg().getIesClient());
            if(!bean.isFitxaCursCreated(expds.get(i), anyacademic))
            {
                bean.createFitxaCurs(expds.get(i), anyacademic);
                n += 1;
            }
                
        }
        return n;        
    }
    
    
    private void comprova()
    {
         listmodel1.removeAllElements();

        String any = (String) jComboBox1.getSelectedItem();
        any = StringUtils.BeforeLast(any, "-");
       

        //QUERY Basada en l'historic
         String SQL1="   SELECT  "+
              "  h.Exp2,  "+
              "  h.AnyAcademic,  "+
              "  h.Ensenyament,  "+
              "  h.Estudis,  "+
              "  h.Grup,  "+
              "  h.ProfTutor,  "+
              "  xal.Llinatge1,  "+
              "  xal.Llinatge2,  "+
              "  xal.Nom1   "+
            "  FROM  `"+CoreCfg.core_mysqlDBPrefix+"`.xes_alumne_historic AS h   "+
              "  INNER JOIN `"+CoreCfg.core_mysqlDBPrefix+"`.xes_alumne AS xal   "+
              "  ON xal.Exp2 = h.Exp2 AND h.AnyAcademic = '"+any+"' "+
            "  WHERE    "+
              "  h.Exp2 NOT IN   "+
              "  (SELECT   "+
              "    a.Exp_FK_ID AS Exp2   "+
              "  FROM  `"+CoreCfg.core_mysqlDBPrefix+"`.fitxa_alumne_curs AS a   "+
              "  WHERE  a.IdCurs_FK_ID = '"+any+"')";


        //System.out.println(SQL1);
 
        int casos=0;
        try {
              Statement st = cfg.getCoreCfg().getMysql().createStatement();
              ResultSet rs1 = cfg.getCoreCfg().getMysql().getResultSet(SQL1,st);
              
            while (rs1 != null && rs1.next()) {
                int expd = rs1.getInt("Exp2");
                String nom = rs1.getString("Llinatge1") + " " + rs1.getString("Llinatge2") +", " +rs1.getString("Nom1");
                listmodel1.addElement(nom+ " ["+expd+"] ");
                casos +=1;
            }
            rs1.close();
            st.close();
        } catch (SQLException ex) {
            Logger.getLogger(CreaFitxes.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        jTextArea1.append("> Hi ha "+casos+" alumnes sense la fitxa del curs "+any+" creada.\n");
    } 
    
