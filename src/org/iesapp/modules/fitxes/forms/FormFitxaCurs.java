     public BeanFitxaCurs getData()
     {
        BeanFitxaCurs b = new BeanFitxaCurs(cfg.getCoreCfg().getIesClient());

        b.setProfessor(jProfessor.getText());
        b.setGrup( (String) jGrup.getSelectedItem());
        b.setCurs( (String) jEstudis.getSelectedItem());
        b.setNivell( (String) jEnsenyament.getSelectedItem());
        b.setAny_academic(jAny_academic.getText());
        String aux = jNotaMitjanaFinal.getText();
        aux = aux.replaceAll(",", ".");
        float result = 0;
        try{
            result = (float) Double.parseDouble(aux);
        }
        catch(java.lang.NumberFormatException ex)
        {
            result = (float)  bean.getNotaMitjanaFinal();
        }
        b.setNotaMitjanaFinal(result);
        b.setDeriva_ORI(jDerivaORI.isSelected()? 1 : 0);
        b.setMotiuDerivacioORI(jMotiuDerivacioORI.getText());
        b.setObservacions(jObservacions.getText());
        b.setNumMateriesSuspesesJuny(jNumMateriesSuspesesJuny.getValue());
        b.setDataCreacio(jCreador.getText());

        int ncanvis = BeanFitxaCurs.getChanges(bean, b);
        
        if(isEditable && ncanvis>0)
        {
            String abrev =cfg.getCoreCfg().getUserInfo().getAbrev();
            String str;
            if(!abrev.equals("ADMIN") && !abrev.equals("GUARD")) {
                str = cfg.abrev2prof.get(abrev);
            }
            else {
                str = abrev;
            }
            b.setModificat(StringUtils.noNull(str));
            DataCtrl cd = new DataCtrl();
            b.setDataModificacio( cd.getDataSQL()+ " "+ cd.getHora());
        }

        String prog = "";
        for(int i=0; i<modelList1.getSize(); i++)
        {
            prog += modelList1.getElementAt(i) + ";";
        }
        prog = StringUtils.BeforeLast(prog, ";");

        b.setProgrames(prog);
        b.setSancions(jSancions.getText());

        return b;
      }


    public void setEditable(boolean editable, Profile profile) {


          boolean visible = true;
          String fullAnyAcademic = cfg.anyAcademicFitxes+"-"+(cfg.anyAcademicFitxes+1);
          if(bean.getAny_academic().equals(fullAnyAcademic))
          {            
                visible = FitxesGUI.moduleGrant.isGranted("fitxaActual_view", profile);
                
           }
           else
           {
               visible = FitxesGUI.moduleGrant.isGranted("fitxaAnterior_view", profile);
          }

                jPanel7.setVisible(!visible);
                jPanel8.setVisible(visible);
                jPanel6.setVisible(visible);


                jProfessor.setEditable(editable);


                //!alerta
                //jEstudis.setEnabled(editable);
                //jGrup.setEditable(editable);
                //jEnsenyament.setEditable(editable);

                //Editable 2:: per aquells camps que són importarts de l'SGD
                boolean editable2 = editable && FitxesGUI.moduleGrant.isGranted("imported_edit", profile);
                      

                if(editable2) {
                    toolTip_imported = "";
                }
                else {
                    toolTip_imported = "Aquestes dades s'importen automàticament";
                }

                jNotaMitjanaFinal.setEditable(editable);
                jNumMateriesSuspesesJuny.setEnabled(editable);
                jDerivaORI.setEnabled(editable);
                jMotiuDerivacioORI.setEditable(editable);
                jObservacions.setEditable(editable);
                jSancions.setEditable(editable);
                jList1.setEnabled(editable);
                jComboBox1.setEnabled(editable);
                jButton1.setEnabled(editable);

                isEditable = editable;
                
    }

