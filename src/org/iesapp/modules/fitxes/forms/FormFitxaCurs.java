    public void setData(BeanFitxaCurs b, Cfg cfg)
    {
            this.cfg = cfg;
            bean = b;

            //System.out.println(b.getAny_academic()+b.getCurs()+b.getGrup()+b.getNivell());
            
            jProfessor.setText(b.getProfessor());

            jEstudis.setSelectedItem(b.getCurs());
           
            jGrup.setSelectedItem(b.getGrup());
            jEnsenyament.setSelectedItem(b.getNivell());

            jAny_academic.setText(b.getAny_academic());
            jNotaMitjanaFinal.setText( "" + b.getNotaMitjanaFinal());
            jNumMateriesSuspesesJuny.setValue(b.getNumMateriesSuspesesJuny());
            jDerivaORI.setSelected(b.getDeriva_ORI()>0);
            jMotiuDerivacioORI.setText(b.getMotiuDerivacioORI());
            jObservacions.setText(b.getObservacions());
 
          //  jNExpDisciplina.setValue(b.getNExpDisciplina());
            jSancions.setText(b.getSancions());

            String prog = b.getProgrames();
            ArrayList<String> programes = StringUtils.parseStringToArray(prog, ";", StringUtils.CASE_INSENSITIVE);
            modelList1.clear();
            for(int i=0; i<programes.size(); i++)
            {
                modelList1.addElement(programes.get(i));
            }
           

            jCreador.setText(b.getDataCreacio());
            jModificat.setText(b.getModificat());
            jDataModificacio.setText(b.getDataModificacio());


            if(cfg.getCoreCfg().getUserInfo().getGrant()!=User.ADMIN)
            {
                //if(!jProfessor.getText().equals("")) jProfessor.setEditable(false);
                //System.out.println(jEstudis.getSelectedIndex()+", "+ (String)jEstudis.getSelectedItem());
                if(jEstudis.getSelectedIndex()>=0) {
                    jEstudis.setEnabled(false);
                }
                if(jGrup.getSelectedIndex()>=0) {
                    jGrup.setEnabled(false);
                }
                if(jEnsenyament.getSelectedIndex()>=0) {
                    jEnsenyament.setEnabled(false);
                }
            }

            //fill Table
            while(jTable1.getRowCount()>0)
            {
                modelTable1.removeRow(0);
            }
            
            for(String ky: bean.getImportacioSGD().keySet())
            {
                SgdInc sgdinc = bean.getImportacioSGD().get(ky);
                modelTable1.addRow(new Object[]{ky, sgdinc.getN1a(),sgdinc.getN2a(), sgdinc.getN3a() });
            }
            
    }

